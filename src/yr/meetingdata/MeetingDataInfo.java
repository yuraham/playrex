package yr.meetingdata;

public class MeetingDataInfo {
	private int data_numb;
	private String data_name;
	private String data_realName;
	private int data_met;
	
	public int getData_numb() {
		return data_numb;
	}
	public void setData_numb(int data_numb) {
		this.data_numb = data_numb;
	}
	public String getData_name() {
		return data_name;
	}
	public void setData_name(String data_name) {
		this.data_name = data_name;
	}
	public String getData_realName() {
		return data_realName;
	}
	public void setData_realName(String data_realName) {
		this.data_realName = data_realName;
	}
	public int getData_met() {
		return data_met;
	}
	public void setData_met(int data_met) {
		this.data_met = data_met;
	}
}
