package yr.participant;

public class ParticipantInfo {
	private int par_numb;
	private int par_enum;
	private int par_met;
	
	public int getPar_numb() {
		return par_numb;
	}
	public void setPar_numb(int par_numb) {
		this.par_numb = par_numb;
	}
	public int getPar_enum() {
		return par_enum;
	}
	public void setPar_enum(int par_enum) {
		this.par_enum = par_enum;
	}
	public int getPar_met() {
		return par_met;
	}
	public void setPar_met(int par_met) {
		this.par_met = par_met;
	}
}
